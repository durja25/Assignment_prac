package loop;

public class Elseif_demo {
	public static void main(String[] args) {
		
	int num =1;
	if(num == 1) {
		System.out.println("one");
	}else if (num ==2) {
		System.out.println("two");
	}else if(num ==3) {
		System.out.println("three");
	}else {
		System.out.println("Wrong Input");
	}

	}
		
		
}
