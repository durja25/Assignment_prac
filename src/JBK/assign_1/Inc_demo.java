package JBK.assign_1;

public class Inc_demo {
	public static void main(String[] args) {
		int i = 50;
		System.out.println("The Value is " + i + ".");
		// i++ keeps and incs after that stmt
		System.out.println("The Value of i++ is " + i++ + ".");
		// ++i incs first that stmt then prints
		System.out.println("The va;ue of ++i is " + ++i + ".");
		System.out.println("The va;ue of ++i is " + ++i + ".");
	}
}
